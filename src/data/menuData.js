// Flat dish lookup by id
export function getDishById(dishId) {
  for (const cat of categories) {
    for (const sub of cat.subcategories) {
      const dish = sub.dishes.find((d) => d.id === dishId);
      if (dish) return { dish, category: cat, subcategory: sub };
    }
  }
  return null;
}

export function getCategoryById(catId) {
  return categories.find((c) => c.id === catId) || null;
}

export function getAllDishes() {
  return categories.flatMap((c) => c.subcategories.flatMap((s) => s.dishes));
}
